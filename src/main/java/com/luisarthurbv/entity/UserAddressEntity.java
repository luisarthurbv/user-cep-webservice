package com.luisarthurbv.entity;

import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "user_address")
public class UserAddressEntity {

    @Id
    private long id;
    private long userId;
    private String cep;
    private String street;
    private String number;
    private String complements;
    private String neighborhood;
    private String city;
    private String state;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public long getUserId() {
        return userId;
    }

    public void setUserId(long userId) {
        this.userId = userId;
    }

    public String getCep() {
        return cep;
    }

    public void setCep(String cep) {
        this.cep = cep;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getComplements() {
        return complements;
    }

    public void setComplements(String complements) {
        this.complements = complements;
    }

    public String getNeighborhood() {
        return neighborhood;
    }

    public void setNeighborhood(String neighborhood) {
        this.neighborhood = neighborhood;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public void mergeAddress(UserAddressEntity entity) {
        if(entity.cep != null) {
            this.cep = entity.cep;
        }
        if(entity.street != null) {
            this.street = entity.street;
        }
        if(entity.number != null) {
            this.number = entity.number;
        }
        if(entity.complements != null) {
            this.complements = entity.complements;
        }
        if(entity.neighborhood != null) {
            this.neighborhood = entity.neighborhood;
        }
        if(entity.city != null) {
            this.city = entity.city;
        }
        if(entity.state != null) {
            this.state = entity.state;
        }
    }
}
